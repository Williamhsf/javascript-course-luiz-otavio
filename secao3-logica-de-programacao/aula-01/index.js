/*
Operadores de comparação
- > maior que
- >= maior que ou igual a
- < menor que
- <= menor que ou igual a
- === igualdade estrita (valor e tipo)
- !== diferente estrito (valor e tipo)

* === e !== (são os mais utilizados pois checam valores e tipos) 
*/
console.log("======teste de (=== e !==)======")
const num1 = 10;
const num2 = 10;

const resultado1 = (num1 === num2)
const resultado2 = (num1 !== num2)

console.log(`Resultado 1 é? ${resultado1}`)
console.log(`Resultado 2 é? ${resultado2}`)
